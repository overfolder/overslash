#!/usr/bin/env bash
# Detects worktree context and writes .env.local with isolated ports.
# Idempotent — safe to call multiple times.

set -euo pipefail

# Required commands. `ss` is Linux-only — fail loudly on macOS/BSD rather than
# silently picking a port we never actually verified to be free.
for cmd in ss cksum awk grep; do
    if ! command -v "$cmd" >/dev/null 2>&1; then
        echo "worktree-env.sh: required command '$cmd' not found in PATH" >&2
        exit 1
    fi
done

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
ENV_LOCAL="$REPO_ROOT/.env.local"

# Detect worktree: .git is a file (not directory) in worktrees
if [ -d "$REPO_ROOT/.git" ]; then
    # Main repo — nothing to do
    exit 0
fi

# Extract worktree ID from path: .cline/worktrees/<ID>/reponame
WORKTREE_ID=$(echo "$REPO_ROOT" | grep -oP '\.cline/worktrees/\K[^/]+' || true)
if [ -z "$WORKTREE_ID" ]; then
    exit 0  # Not a recognized worktree path
fi

# If .env.local already exists for this worktree, reuse it
if [ -f "$ENV_LOCAL" ] && grep -q "overslash-wt-${WORKTREE_ID}" "$ENV_LOCAL" 2>/dev/null; then
    exit 0
fi

# Derive base ports deterministically from worktree ID. Each service gets its
# own range so multiple worktrees can run `make dev` concurrently without
# colliding on api (3000) or dashboard (5173).
HASH=$(echo "$WORKTREE_ID" | cksum | awk '{print $1}')
PG_BASE=$(( (HASH % 9000) + 56000 ))
API_BASE=$(( (HASH % 9000) + 13000 ))
DASH_BASE=$(( (HASH % 9000) + 25000 ))
# Standalone `overslash web` binary. Separate from API_BASE so the Docker API
# container (compose) and the bare binary can coexist within one worktree.
WEB_BASE=$(( (HASH % 9000) + 18000 ))
# MCP runtime service (node process hosting third-party MCP servers).
MCP_RUNTIME_BASE=$(( (HASH % 9000) + 21000 ))

# Find a free port starting at $1, scanning up to 20 above. Echoes the chosen
# port, or exits non-zero on exhaustion.
find_free_port() {
    local base="$1" name="$2" i port
    for i in $(seq 0 20); do
        port=$(( base + i ))
        if ! ss -tlnp 2>/dev/null | grep -q ":${port} "; then
            echo "$port"
            return 0
        fi
    done
    echo "worktree-env.sh: no free $name port in range ${base}-$((base + 20))" >&2
    return 1
}

PG_PORT=$(find_free_port "$PG_BASE" postgres) || exit 1
API_PORT=$(find_free_port "$API_BASE" api) || exit 1
DASH_PORT=$(find_free_port "$DASH_BASE" dashboard) || exit 1
WEB_PORT=$(find_free_port "$WEB_BASE" web) || exit 1
MCP_RUNTIME_PORT=$(find_free_port "$MCP_RUNTIME_BASE" mcp-runtime) || exit 1

# Compose project name (lowercase, alphanumeric + hyphens)
PROJECT_NAME="overslash-wt-${WORKTREE_ID}"

# Write .env.local (overrides .env values). docker-compose.dev.yml reads
# PG_HOST_PORT, API_HOST_PORT, DASH_HOST_PORT. OVERSLASH_WEB_PORT is read by
# the `overslash web` binary (crates/overslash-cli/src/web.rs) and by
# dashboard/vite.config.ts when proxying to the worktree's standalone backend.
cat > "$ENV_LOCAL" <<EOF
# Auto-generated by bin/worktree-env.sh — do not edit
COMPOSE_PROJECT_NAME=${PROJECT_NAME}
PG_HOST_PORT=${PG_PORT}
API_HOST_PORT=${API_PORT}
DASH_HOST_PORT=${DASH_PORT}
OVERSLASH_WEB_PORT=${WEB_PORT}
MCP_RUNTIME_HOST_PORT=${MCP_RUNTIME_PORT}
DATABASE_URL=postgres://overslash:overslash@localhost:${PG_PORT}/overslash
EOF

echo "Worktree env: project=${PROJECT_NAME} pg=${PG_PORT} api=${API_PORT} dashboard=${DASH_PORT} web=${WEB_PORT} mcp-runtime=${MCP_RUNTIME_PORT}"
